public class Date{
	public int day;
	public int month;
	public int year;

	public void setDay(int day){
		this.day = day;
	}
	
	public int getDay(){
		return day;
	}

	public void setMonth(int month){
		this.month = month;
	}
	
	public int getMonth(){
		return month;
	}

	public void setYear(int year){
		this.year = year;
	}
	
	public int getYear(){
		return year;
	}


}