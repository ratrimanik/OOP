public class Array{
	public static void main(String args[]){
		int[] nilai; //pendeklarasian array

		nilai = new int[3];

		nilai[0] = 80; //pemberian nilai array
		nilai[1] = 85;
		nilai[2] = 75;

		System.out.println(nilai[0]); //pengaksesan elemen array
		System.out.println(nilai[1]);	
		System.out.println(nilai[2]);
	} 
}